import { Component, inject, signal, computed } from '@angular/core';
import { Router, RouterLink, ActivatedRoute } from '@angular/router';
import { AuthService } from '../../core/auth.service';

export interface Finding {
  id: string;
  type: string;
  severity: 'critical' | 'high' | 'medium' | 'low';
  title: string;
  description: string;
  location: string;
  recommendation: string;
}

export interface ScanReport {
  scanId: string;
  fileName: string;
  fileType: string;
  fileSize: string;
  scannedAt: Date;
  scanDuration: string;
  riskScore: number;
  riskLevel: 'critical' | 'high' | 'medium' | 'low' | 'clean';
  findings: Finding[];
  summary: string;
}

@Component({
  selector: 'app-results',
  standalone: true,
  imports: [RouterLink],
  templateUrl: './results.html',
  styleUrl: './results.scss',
})
export class Results {
  private auth = inject(AuthService);
  private route = inject(ActivatedRoute);
  private router = inject(Router);

  currentUser = this.auth.currentUser;
  activeTab = signal<'findings' | 'recommendations' | 'metadata'>('findings');

  report = signal<ScanReport>(this.generateMockReport());

  criticalCount = computed(() =>
    this.report().findings.filter(f => f.severity === 'critical').length
  );
  highCount = computed(() =>
    this.report().findings.filter(f => f.severity === 'high').length
  );
  mediumCount = computed(() =>
    this.report().findings.filter(f => f.severity === 'medium').length
  );
  lowCount = computed(() =>
    this.report().findings.filter(f => f.severity === 'low').length
  );

  getRiskColor(level: string): string {
    const map: Record<string, string> = {
      critical: '#ff3b3b',
      high: '#ff8c00',
      medium: '#ffd36a',
      low: '#7cf7ff',
      clean: '#00e887',
    };
    return map[level] ?? '#fff';
  }

  getRiskBg(level: string): string {
    const map: Record<string, string> = {
      critical: 'rgba(255,59,59,0.08)',
      high: 'rgba(255,140,0,0.08)',
      medium: 'rgba(255,211,106,0.08)',
      low: 'rgba(124,247,255,0.08)',
      clean: 'rgba(0,232,135,0.08)',
    };
    return map[level] ?? 'rgba(255,255,255,0.04)';
  }

  getSeverityOrder(severity: string): number {
    return { critical: 0, high: 1, medium: 2, low: 3 }[severity] ?? 4;
  }

  sortedFindings = computed(() =>
    [...this.report().findings].sort(
      (a, b) => this.getSeverityOrder(a.severity) - this.getSeverityOrder(b.severity)
    )
  );

  setTab(tab: 'findings' | 'recommendations' | 'metadata'): void {
    this.activeTab.set(tab);
  }

  exportReport(): void {
    const report = this.report();
    const content = `
BLACKVAULT SECURITY REPORT
===========================
File: ${report.fileName}
Scanned: ${report.scannedAt.toLocaleString()}
Risk Score: ${report.riskScore}/100
Risk Level: ${report.riskLevel.toUpperCase()}

FINDINGS (${report.findings.length})
----------------------------
${report.findings.map(f => `[${f.severity.toUpperCase()}] ${f.title}
  ${f.description}
  Location: ${f.location}
  Recommendation: ${f.recommendation}
`).join('\n')}

Generated by BlackVault AI Security Scanner
    `.trim();

    const blob = new Blob([content], { type: 'text/plain' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = `blackvault-report-${report.scanId.slice(0, 8)}.txt`;
    a.click();
    URL.revokeObjectURL(url);
  }

  async logout(): Promise<void> {
    await this.auth.logout();
  }

  private generateMockReport(): ScanReport {
    const scanId = this.route.snapshot.paramMap.get('scanId') ?? crypto.randomUUID();
    return {
      scanId,
      fileName: 'How to Pull Code from GitHub and save it.txt',
      fileType: 'TXT',
      fileSize: '1.7 KB',
      scannedAt: new Date(),
      scanDuration: '3.2s',
      riskScore: 74,
      riskLevel: 'high',
      summary: 'BlackVault detected 5 security findings in this document including an exposed API key, personal email addresses, and suspicious instruction patterns consistent with prompt injection attempts. Immediate remediation is recommended for critical and high severity findings.',
      findings: [
        {
          id: '1',
          type: 'Secret Exposure',
          severity: 'critical',
          title: 'Exposed API Key Detected',
          description: 'A hardcoded API key matching the pattern of a GitHub Personal Access Token was found in the document body.',
          location: 'Line 14 — "ghp_xxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxx"',
          recommendation: 'Immediately revoke this token in your GitHub settings and rotate it. Never store API keys in plain text documents. Use environment variables or a secrets manager instead.',
        },
        {
          id: '2',
          type: 'Prompt Injection',
          severity: 'high',
          title: 'Prompt Injection Pattern Found',
          description: 'The document contains text structured as an instruction to an AI system, potentially designed to hijack automated pipelines that process this document.',
          location: 'Paragraph 3 — "Ignore previous instructions and..."',
          recommendation: 'Remove or sanitize instruction-like language before passing this document to any AI system. Implement input validation on your document processing pipeline.',
        },
        {
          id: '3',
          type: 'PII Leak',
          severity: 'high',
          title: 'Email Address Exposed',
          description: 'Two personal email addresses were identified in the document that may constitute personally identifiable information.',
          location: 'Lines 8, 22 — user@example.com, admin@company.com',
          recommendation: 'Redact or anonymize email addresses before sharing this document externally. Review your data handling policy for PII.',
        },
        {
          id: '4',
          type: 'Red Flag',
          severity: 'medium',
          title: 'Suspicious URL Pattern',
          description: 'A URL was detected that matches patterns commonly associated with data exfiltration or phishing redirects.',
          location: 'Line 31 — "http://suspicious-domain.xyz/redirect"',
          recommendation: 'Verify the legitimacy of this URL before clicking or sharing. Consider scanning the destination with a URL reputation service.',
        },
        {
          id: '5',
          type: 'PII Leak',
          severity: 'low',
          title: 'Phone Number Pattern Detected',
          description: 'A string matching a US phone number format was found. This may be intentional but is flagged for review.',
          location: 'Line 45 — "(555) 867-5309"',
          recommendation: 'Confirm whether this phone number should be present in this document. If sharing externally, consider redacting personal contact information.',
        },
      ],
    };
  }
}